export function getTemplatesForProject(input: ProjectCreationInput): TaskTemplate[] {
  const family = familyForProjectType(input.projectType);
  return TEMPLATES[family][input.pathwayMode];
}

export interface PathwayTokens {
  niche: string;
  audience: string;
  timeAvailability: string;
}

/**
 * Deterministic pathway generation. AI refinement (src/lib/actions/buildAi.ts)
 * may rewrite this text afterward, but this function alone must always be
 * able to produce a complete, usable pathway with no AI involved.
 */
export function generatePathway(
  input: ProjectCreationInput,
  locale: Locale | string,
  tokens: PathwayTokens
): GeneratedTask[] {
  const templates = getTemplatesForProject(input);
  const l = locale === "ru" ? "ru" : "en";

  return templates.map((template, index) => ({
    templateId: template.id,
    stage: template.stage,
    orderIndex: index,
    title: fillTokens(template.title[l], tokens),
    objective: fillTokens(template.objective[l], tokens),
    whyItMatters: fillTokens(template.whyItMatters[l], tokens),
    action: fillTokens(template.action[l], tokens),
    expectedOutput: fillTokens(template.expectedOutput[l], tokens),
    estimatedTime: template.estimatedTime[l],
    completionCriteria: fillTokens(template.completionCriteria[l], tokens),
    outputKind: template.outputKind,
    xp: template.xp,
    recommendedLessonId: template.recommendedLessonId,
  }));
}
